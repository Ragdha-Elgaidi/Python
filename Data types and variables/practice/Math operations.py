first = input('Enter first number: ')
first = float(first)

# let's make it in a single line
second = float(input('Enter second number: '))

print(first, '+', second, '=', first + second)
print(first, '-', second, '=', first - second)
print(first, '*', second, '=', first * second)
print(first, '/', second, '=', first / second)

print("\nEnd of program")

